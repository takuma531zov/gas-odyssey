export const promptToAI = (ocrText: string): string => `
あなたはレシートまたはクレジットカード明細の情報を正確に読み取り、以下の形式で項目を抽出するAIです。
ただし、抽出が困難・不明な場合には、null を返してください（JSONではなく単に null とだけ出力してください）。


### 入力されたレシートまたは明細全文:
${ocrText}

### 出力フォーマット（JSON）:
1. レシート形式の場合（複数ある可能性あり）:
[
  {
    "店舗名": string,
    "日付": string,
    "合計金額税込": string,
    "税率8%対象金額税込": string,        // 該当があれば（なければ "0"）
    "税率10%対象金額税込": string,       // 該当があれば（なければ "0"）
    "軽減税率対象あり": boolean     // true または false
  },
  ...
]

2. クレジットカード利用明細の場合（複数ある可能性あり）:
[
  {
    "引き落とし日": string,                // 例: "06/20"
    "店舗名": string,               // 例: "スターバックス"
    "カード名": string,             // 例: "楽天カード"
    "利用日": string,              // 例: "05/31"
    "金額": string,                 // 例: "620"
    "表示用店舗名": string         // 例: "スターバックス（楽天カード 05/31）"
  },
  ...
]

### 注意点:
- 金額は数値のみで、小数点なしの文字列で返してください（例: "1234"）。
- 金額はすべて税込み価格で返してください。税率8%、10%の対象金額も税込みで記載してください。（例：8%対象額 1234 + 8%税額 100 = 税率8%対象金額税込 1334）
- 日付は「MM/DD」形式で返してください。
- 軽減税率が含まれている場合、「税率8%対象金額」と「軽減税率対象あり: true」を出力してください。
（軽減税率の表記は以下のように様々な表記があります。「軽減税率」「軽減税率対象」「8%対象」「税率8%」「8％」「税率8パーセント」などです。）
これらをすべて軽減税率対象ありとして正しく認識してください。
- クレジットカード明細形式かレシート形式かは内容から自動で判断してください。
- 出力は必ず純粋なJSON形式、または null のみで返してください。日本語の補足説明は不要です。
`;
